//Ejercicio 2: Dado un número, determinar si el número es positivo o negativo.

const number = -5; 

if (number > 0) {
    console.log(`El número ${number} es positivo`);
} else if (number < 0) {
    console.log(`El número ${number} es negativo`);
} else {
    console.log(`El número ${number} es neutro`);
}